def makePattern(array, defaultLen, stringData):
	pay1, pay2 = [], {}
	encode = array
	numString = {0:"${#}",1:"${#__}", 2:"${#___}", 3:"${#____}", 4:"${#_____}",
				 5:"${#______}", 6:"${#_______}", 7:"${#________}",
				 8:"${#_________}", 9:"${#__________}"}
	c = "$___________"
	a = "$____________"
	t = "$_____________"
	cmd3_py = "$_______________"
	flagbox = "$___________________"

	underBar = "_"*defaultLen
	retString, retString2 = '', ''
	for ch in stringData:
		if ch=='C' or ch=='A' or ch=='T':
					continue
		else:
			if (ord(ch)>=65 and ord(ch)<=90):
				if ch not in pay1:
					this_string = encode[ch.lower()] % (underBar, underBar, underBar)
					pay1.append(ch)
					pay2[ch] = "$" + underBar
					underBar += "_"
					retString += this_string
			else:
				continue

	for idx in stringData:
		if idx == 'C':
			retString2 += c
			continue
		elif idx == 'A':
			retString2 += a
			continue
		elif idx == 'T':
			retString2 += t
			continue
		if (ord(idx)>=65 and ord(idx)<=90):
			retString2 += pay2[idx]
		else:
			retString2 += numString[int(idx)]

	return retString, retString2

